// Get current language from localStorage or default to English
function getCurrentLanguage() {
    const saved = localStorage.getItem('devlaststudio_lang');
    if (saved && translations[saved]) {
        return saved;
    }
    return 'en'; // Always default to English
}

// Set language
function setLanguage(lang) {
    if (!translations[lang]) return;

    localStorage.setItem('devlaststudio_lang', lang);
    document.documentElement.lang = lang;
    applyTranslations(lang);
    updateLanguageSelector(lang);
}

// Apply translations to the page
function applyTranslations(lang) {
    const t = translations[lang];

    // Find all elements with data-i18n attribute and update them
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            el.textContent = t[key];
        }
    });

    // Find all elements with data-i18n-placeholder attribute and update them
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });
}

// Update language selector display
function updateLanguageSelector(lang) {
    const currentLangEl = document.getElementById('current-lang');
    const currentFlagEl = document.getElementById('current-flag');

    if (currentLangEl) {
        currentLangEl.textContent = lang.toUpperCase();
    }
    if (currentFlagEl) {
        currentFlagEl.textContent = languageFlags[lang];
    }
}

// Initialize language on page load
function initLanguage() {
    const lang = getCurrentLanguage();
    document.documentElement.lang = lang;
    applyTranslations(lang);
    updateLanguageSelector(lang);
}

// Export for use in other files
window.i18n = {
    translations,
    languageNames,
    languageFlags,
    getCurrentLanguage,
    setLanguage,
    initLanguage
};
